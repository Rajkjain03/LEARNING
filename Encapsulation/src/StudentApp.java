
public class StudentApp {
	private int rollNo;
	private String name;
	private int age;
	
	public void setData(int x, String y, int z) {
		rollNo = x;
		name = y;
		age = z;
	}
	
	public int getRollNo() {
		return rollNo;
	}
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
}
